-- 1. CUSTOMER_COMPLAINTS
CREATE TABLE CUSTOMER_COMPLAINTS (
  ID            NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  COMPLAINT_NO  VARCHAR2(30)   NOT NULL,
  CUSTOMER_CODE VARCHAR2(50)   NOT NULL,
  CUSTOMER_NAME VARCHAR2(200),
  COMPLAINT_DATE DATE          NOT NULL,
  ITEM_CODE     VARCHAR2(50),
  LOT_NO        VARCHAR2(100),
  DEFECT_QTY    NUMBER DEFAULT 0,
  COMPLAINT_TYPE VARCHAR2(30)  NOT NULL,
  DESCRIPTION   VARCHAR2(2000),
  URGENCY       VARCHAR2(20)  DEFAULT 'MEDIUM',
  STATUS        VARCHAR2(30)  DEFAULT 'RECEIVED',
  INVESTIGATION VARCHAR2(2000),
  ROOT_CAUSE    VARCHAR2(2000),
  CONTAINMENT_ACTION VARCHAR2(1000),
  CORRECTIVE_ACTION  VARCHAR2(1000),
  PREVENTIVE_ACTION  VARCHAR2(1000),
  RESPONSE_DATE DATE,
  RESPONSIBLE_CODE   VARCHAR2(50),
  CAPA_ID       NUMBER,
  COST_AMOUNT   NUMBER(12,2),
  RESOLVED_AT   TIMESTAMP,
  COMPANY       VARCHAR2(50)  NOT NULL,
  PLANT         VARCHAR2(20)  NOT NULL,
  CREATED_BY    VARCHAR2(50),
  UPDATED_BY    VARCHAR2(50),
  CREATED_AT    TIMESTAMP DEFAULT SYSTIMESTAMP,
  UPDATED_AT    TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE INDEX IDX_CC_STATUS ON CUSTOMER_COMPLAINTS (COMPANY, PLANT, STATUS);
CREATE UNIQUE INDEX IDX_CC_NO ON CUSTOMER_COMPLAINTS (COMPANY, PLANT, COMPLAINT_NO);

-- 2. CHANGE_ORDERS
CREATE TABLE CHANGE_ORDERS (
  ID            NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  CHANGE_NO     VARCHAR2(30)   NOT NULL,
  CHANGE_TYPE   VARCHAR2(30)   NOT NULL,
  TITLE         VARCHAR2(200)  NOT NULL,
  DESCRIPTION   VARCHAR2(2000),
  REASON        VARCHAR2(1000),
  RISK_ASSESSMENT VARCHAR2(1000),
  AFFECTED_ITEMS VARCHAR2(2000),
  AFFECTED_PROCESSES VARCHAR2(2000),
  PRIORITY      VARCHAR2(20)  DEFAULT 'MEDIUM',
  STATUS        VARCHAR2(30)  DEFAULT 'DRAFT',
  REQUESTED_BY  VARCHAR2(50),
  REQUESTED_AT  TIMESTAMP,
  REVIEWER_CODE VARCHAR2(50),
  REVIEWED_AT   TIMESTAMP,
  REVIEW_COMMENT VARCHAR2(1000),
  APPROVER_CODE VARCHAR2(50),
  APPROVED_AT   TIMESTAMP,
  APPROVE_COMMENT VARCHAR2(1000),
  EFFECTIVE_DATE DATE,
  COMPLETION_DATE DATE,
  COMPANY       VARCHAR2(50)  NOT NULL,
  PLANT         VARCHAR2(20)  NOT NULL,
  CREATED_BY    VARCHAR2(50),
  UPDATED_BY    VARCHAR2(50),
  CREATED_AT    TIMESTAMP DEFAULT SYSTIMESTAMP,
  UPDATED_AT    TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE INDEX IDX_CO_STATUS ON CHANGE_ORDERS (COMPANY, PLANT, STATUS);
CREATE UNIQUE INDEX IDX_CO_NO ON CHANGE_ORDERS (COMPANY, PLANT, CHANGE_NO);

-- 3. CAPA_REQUESTS
CREATE TABLE CAPA_REQUESTS (
  ID            NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  CAPA_NO       VARCHAR2(30)   NOT NULL,
  CAPA_TYPE     VARCHAR2(20)   NOT NULL,
  SOURCE_TYPE   VARCHAR2(30),
  SOURCE_ID     NUMBER,
  TITLE         VARCHAR2(200)  NOT NULL,
  DESCRIPTION   VARCHAR2(2000),
  ROOT_CAUSE    VARCHAR2(2000),
  ACTION_PLAN   VARCHAR2(2000),
  TARGET_DATE   DATE,
  RESPONSIBLE_CODE VARCHAR2(50),
  STATUS        VARCHAR2(30)  DEFAULT 'OPEN',
  PRIORITY      VARCHAR2(20),
  VERIFICATION_RESULT VARCHAR2(1000),
  VERIFIED_BY   VARCHAR2(50),
  VERIFIED_AT   TIMESTAMP,
  CLOSED_AT     TIMESTAMP,
  ITEM_CODE     VARCHAR2(50),
  LINE_CODE     VARCHAR2(50),
  COMPANY       VARCHAR2(50)  NOT NULL,
  PLANT         VARCHAR2(20)  NOT NULL,
  CREATED_BY    VARCHAR2(50),
  UPDATED_BY    VARCHAR2(50),
  CREATED_AT    TIMESTAMP DEFAULT SYSTIMESTAMP,
  UPDATED_AT    TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE INDEX IDX_CAPA_STATUS ON CAPA_REQUESTS (COMPANY, PLANT, STATUS);
CREATE UNIQUE INDEX IDX_CAPA_NO ON CAPA_REQUESTS (COMPANY, PLANT, CAPA_NO);

-- 4. CAPA_ACTIONS
CREATE TABLE CAPA_ACTIONS (
  ID            NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  CAPA_ID       NUMBER NOT NULL,
  SEQ           NUMBER NOT NULL,
  ACTION_DESC   VARCHAR2(1000) NOT NULL,
  RESPONSIBLE_CODE VARCHAR2(50),
  DUE_DATE      DATE,
  STATUS        VARCHAR2(20) DEFAULT 'PENDING',
  RESULT        VARCHAR2(1000),
  COMPLETED_AT  TIMESTAMP,
  CREATED_AT    TIMESTAMP DEFAULT SYSTIMESTAMP,
  UPDATED_AT    TIMESTAMP DEFAULT SYSTIMESTAMP,
  CONSTRAINT FK_CAPA_ACTION_REQ FOREIGN KEY (CAPA_ID) REFERENCES CAPA_REQUESTS(ID)
);

CREATE INDEX IDX_CAPA_ACT_CAPAID ON CAPA_ACTIONS (CAPA_ID);

-- 5. FAI_REQUESTS
CREATE TABLE FAI_REQUESTS (
  ID            NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  FAI_NO        VARCHAR2(30)  NOT NULL,
  TRIGGER_TYPE  VARCHAR2(30)  NOT NULL,
  TRIGGER_REF   VARCHAR2(100),
  ITEM_CODE     VARCHAR2(50)  NOT NULL,
  ORDER_NO      VARCHAR2(50),
  LINE_CODE     VARCHAR2(50),
  SAMPLE_QTY    NUMBER DEFAULT 1,
  INSPECTOR_CODE VARCHAR2(50),
  STATUS        VARCHAR2(30) DEFAULT 'REQUESTED',
  INSPECT_DATE  DATE,
  RESULT        VARCHAR2(20),
  REMARKS       VARCHAR2(1000),
  APPROVAL_CODE VARCHAR2(50),
  APPROVED_AT   TIMESTAMP,
  COMPANY       VARCHAR2(50)  NOT NULL,
  PLANT         VARCHAR2(20)  NOT NULL,
  CREATED_BY    VARCHAR2(50),
  UPDATED_BY    VARCHAR2(50),
  CREATED_AT    TIMESTAMP DEFAULT SYSTIMESTAMP,
  UPDATED_AT    TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE INDEX IDX_FAI_STATUS ON FAI_REQUESTS (COMPANY, PLANT, STATUS);
CREATE UNIQUE INDEX IDX_FAI_NO ON FAI_REQUESTS (COMPANY, PLANT, FAI_NO);

-- 6. FAI_ITEMS
CREATE TABLE FAI_ITEMS (
  ID            NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  FAI_ID        NUMBER NOT NULL,
  SEQ           NUMBER NOT NULL,
  INSPECT_ITEM  VARCHAR2(200) NOT NULL,
  SPEC_MIN      NUMBER(12,4),
  SPEC_MAX      NUMBER(12,4),
  MEASURED_VALUE NUMBER(12,4),
  UNIT          VARCHAR2(20),
  RESULT        VARCHAR2(10),
  REMARKS       VARCHAR2(500),
  CREATED_AT    TIMESTAMP DEFAULT SYSTIMESTAMP,
  UPDATED_AT    TIMESTAMP DEFAULT SYSTIMESTAMP,
  CONSTRAINT FK_FAI_ITEM_REQ FOREIGN KEY (FAI_ID) REFERENCES FAI_REQUESTS(ID)
);

CREATE INDEX IDX_FAI_ITEM_FAIID ON FAI_ITEMS (FAI_ID);
