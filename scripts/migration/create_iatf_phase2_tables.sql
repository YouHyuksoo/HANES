-- 1. SPC_CHARTS
CREATE TABLE SPC_CHARTS (
  ID                   NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  CHART_NO             VARCHAR2(30)    NOT NULL,
  ITEM_CODE            VARCHAR2(50)    NOT NULL,
  PROCESS_CODE         VARCHAR2(50)    NOT NULL,
  CHARACTERISTIC_NAME  VARCHAR2(200)   NOT NULL,
  CHART_TYPE           VARCHAR2(30)    NOT NULL,
  SUBGROUP_SIZE        NUMBER(10)      DEFAULT 5 NOT NULL,
  USL                  NUMBER(12,4),
  LSL                  NUMBER(12,4),
  TARGET               NUMBER(12,4),
  UCL                  NUMBER(12,4),
  LCL                  NUMBER(12,4),
  CL                   NUMBER(12,4),
  STATUS               VARCHAR2(20)    DEFAULT 'ACTIVE' NOT NULL,
  COMPANY              VARCHAR2(50)    NOT NULL,
  PLANT                VARCHAR2(20)    NOT NULL,
  CREATED_BY           VARCHAR2(50),
  UPDATED_BY           VARCHAR2(50),
  CREATED_AT           TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
  UPDATED_AT           TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL
);

CREATE UNIQUE INDEX UQ_SPC_CHARTS_CHART_NO ON SPC_CHARTS (COMPANY, PLANT, CHART_NO);
CREATE INDEX IX_SPC_CHARTS_ITEM ON SPC_CHARTS (COMPANY, PLANT, ITEM_CODE);
CREATE INDEX IX_SPC_CHARTS_PROCESS ON SPC_CHARTS (COMPANY, PLANT, PROCESS_CODE);

