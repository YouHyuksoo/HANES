import openpyxl
from openpyxl import Workbook
from openpyxl.styles import Font, PatternFill, Alignment, Border, Side
from openpyxl.utils import get_column_letter

wb = Workbook()
hf = Font(bold=True, color='FFFFFF', size=11)
hfill = PatternFill('solid', fgColor='2B579A')
af = PatternFill('solid', fgColor='F5F9FC')
bd = Border(left=Side('thin',color='CCCCCC'),right=Side('thin',color='CCCCCC'),top=Side('thin',color='CCCCCC'),bottom=Side('thin',color='CCCCCC'))
ct = Alignment(horizontal='center',vertical='center',wrap_text=True)
lw = Alignment(vertical='center',wrap_text=True)

def hdr(ws, headers, widths):
    for i,(h,w) in enumerate(zip(headers,widths),1):
        c=ws.cell(row=1,column=i,value=h); c.font=hf; c.fill=hfill; c.alignment=ct; c.border=bd
        ws.column_dimensions[get_column_letter(i)].width=w

def fill_rows(ws, data, center_cols=None):
    center_cols = center_cols or []
    for ri,row in enumerate(data,2):
        for ci,val in enumerate(row,1):
            c=ws.cell(row=ri,column=ci,value=val); c.border=bd
            c.alignment=ct if ci in center_cols else lw
            if ri%2==0: c.fill=af

